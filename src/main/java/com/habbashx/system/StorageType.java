package com.habbashx.system;

/**
 * Represents the different types of storage devices.
 * This enumeration is used to determine and categorize storage media based on their technology.
 *
 * Enumerated Constants:
 * - SSD: Represents a Solid-State Drive, a storage device known for its high speed and reliability.
 * - HDD: Represents a Hard Disk Drive, a storage device known for its lower cost and higher capacity compared to SSDs.
 *
 * This enum is utilized throughout various classes and methods to identify and configure storage-related operations
 * based on the underlying storage type.
 */
public enum StorageType {
    SSD,
    HDD
}
