package com.habbashx.system;

/**
 * Represents types of operating systems.
 * This enumeration defines specific operating system types that the application can identify and process.
 *
 * Enumerated Constants:
 * - WINDOWS: Represents the Microsoft Windows operating system.
 * - LINUX: Represents the Linux operating systems.
 * - MACOS: Represents macOS, the operating system for Apple computers.
 *
 * This enum is primarily used to determine the current operating system environment
 * and to execute OS-specific operations or commands.
 */
enum OperatingSystemType {
    WINDOWS,
    LINUX,
    MACOS,
}
